export function isValidInteriorTile(
  island: IslandType,
  x: number,
  y: number,
): boolean {
  return INTERIOR_LAYOUTS[island].has(`${x},${y}`);
}

/**
 * Returns true iff every cell of the given bounding box is a valid interior
 * tile for the given island. Uses the same (x, y = top) / (y - dy) convention
 * as the rest of the placement system.
 */
export function isValidInteriorBox(
  island: IslandType,
  box: { x: number; y: number; width: number; height: number },
): boolean {
  for (let dx = 0; dx < box.width; dx++) {
    for (let dy = 0; dy < box.height; dy++) {
      if (!isValidInteriorTile(island, box.x + dx, box.y - dy)) {
        return false;
      }
    }
  }
  return true;
}


export function isValidHomeExpansionTile(
  tier: HomeExpansionTier,
  x: number,
  y: number,
): boolean {
  return HOME_EXPANSION_LAYOUTS[tier].has(`${x},${y}`);
}

export function isValidHomeExpansionBox(
  tier: HomeExpansionTier,
  box: { x: number; y: number; width: number; height: number },
): boolean {
  for (let dx = 0; dx < box.width; dx++) {
    for (let dy = 0; dy < box.height; dy++) {
      if (!isValidHomeExpansionTile(tier, box.x + dx, box.y - dy)) {
        return false;
      }
    }
  }
  return true;
}

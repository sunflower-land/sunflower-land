/**
 * Ordered tier list. The player buys the next tier in this list each time they
 * use the `interior.upgrade` event.
 */
export const HOME_EXPANSION_TIER_ORDER: readonly HomeExpansionTier[] = [
  "level-one-start",
  "level-one-2",
  "level-one-3",
  "level-one-4",
  "level-one-5",
  "level-one-6",
  "level-one-full",
] as const;

/**
 * Returns the next tier after the given one, or `null` if maxed out.
 */
export function nextHomeExpansionTier(
  current: HomeExpansionTier,
): HomeExpansionTier | null {
  const idx = HOME_EXPANSION_TIER_ORDER.indexOf(current);
  if (idx < 0 || idx >= HOME_EXPANSION_TIER_ORDER.length - 1) return null;
  return HOME_EXPANSION_TIER_ORDER[idx + 1];
}

